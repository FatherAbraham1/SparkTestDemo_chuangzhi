package cn.wh.tools;

public class user {
	
	public long id;
	public long RegionCoding;
	public long CellCoding;
	public long time;
	
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public long getRegionCoding() {
		return RegionCoding;
	}
	public void setRegionCoding(long regionCoding) {
		RegionCoding = regionCoding;
	}
	public long getCellCoding() {
		return CellCoding;
	}
	public void setCellCoding(long cellCoding) {
		CellCoding = cellCoding;
	}
	public long getTime() {
		return time;
	}
	public void setTime(long time) {
		this.time = time;
	}
	
	

}
